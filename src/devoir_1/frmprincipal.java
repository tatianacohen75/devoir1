    private void txtageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtageActionPerformed
        // TODO add your handling code here:
        //execice 1
        if (txtage.getText().compareTo("")==0){
             JOptionPane.showMessageDialog(this,"entrer une valeur"); 
        }
        
        if (sldnbAnnees.getValue()>5){
            lblprime.setText("non");
            
        }
        else {
            if (cbosexe.getSelectedItem().toString().compareTo("Homme")>0 && Integer.parseInt(txtage.getText())>22){
                lblprime.setText("paye la surprime");
            
        }
            else {
                if (Integer.parseInt(txtage.getText())<30 & Integer.parseInt(txtage.getText())>20){
                    lblprime.setText("payer la surprime");
                    
                }
            }
        }
        
       

        
    }//GEN-LAST:event_txtageActionPerformed

